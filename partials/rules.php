<h2>Regole del gioco</h2>

<p>L'impiccato è un famosissimo gioco che consiste nel provare ad indovinare una frase nascosta,
lettera per lettera. Inizialmente sono vibili solamente la lunghezza di ogni parola all'interno
della frase e la posizione degli spazi.</p>

<ul>
    <li>Scegli la modalità utilizzando i due bottoni presenti sotto</li>

    <li>Se hai scelto multiplayer, il giocatore due inserisce una parola facendo attenzione che il 
    giocatore uno non la riesca a vedere</li>
    
    <li>Verrai reindirizzato ad una pagina in cui puoi inserire una singola lettera con il box a destra
    o l'intera frase con il box a sinistra. Hai a disposizione sette errori, dopo i quali avrai perso
    la partita.</li>

    <li>Alla fine del gioco, in caso di vittoria potrai inserire il tuo username ed il tuo nome verrà
    visualizzato nella <a href="leaderboard.php">leaderboard</a>.</li>
</ul>
